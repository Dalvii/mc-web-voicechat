package com.dalvi.models;

public class PlayerData {
    public double x, y, z;
    public float yaw, pitch;

    public PlayerData(double x, double y, double z, float yaw, float pitch) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.yaw = yaw;
        this.pitch = pitch;
    }
}
